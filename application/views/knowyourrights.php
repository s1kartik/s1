 <p><strong>What if the Employer does not follow the ESA?</strong></p>
<p>If an employee feels that the ESA is not being complied with, they can contact the Employment Standards Information Centre at 416-326-7160/1-800-531-555</p>
<p><strong>How are Complaints Handled?</strong></p>
<p>Complaints are investigated by an employment standards officer (ESO), who can provide orders against employers including orders to comply with the ESA.  ESO's can require voluntary compliance, issuing orders over pay wages, and orders to reinstate and/or compensate a notice of contravention or issue other tickets for prosecution under the Provincial Offenses Act.</p>
<p><strong>Are Sick and Bereavement Leaves covered by the ESA?</strong></p>
<p>Employees who have employers that employ at least 50 workers are entitled to personal emergency leave in certain situations.  Some employers have paid benefit plans for sickness, bereavement and other leave of absences; these plans aren't required by the ESA.  </p>
<p><strong>If an Employee is off sick can he/she be fired?</strong></p>
<p>The employee cannot be penalized for taking a day off; personal emergencies and job protection are covered under the Act.</p>
<p><strong>Do Employees need to give notice to their employers when they quit their jobs?</strong></p>
<p>The ESA doesn't cover the issue of employees giving notice to the employer when they quit their jobs, except under the pregnancy and parental leave section.  Employees may be required to provide their employers with notice they are leaving.</p>
<p><strong>Can Employees choose to take their employers to court for wrongful dismissal rather then file a claim?</strong></p>
<p>An employee can choose to sue in a court of law for wrongful dismissal; however an employee can't sue and file a claim over the same issue.  </p>
