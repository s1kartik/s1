<h4 id="tl-3">Connections</h4>

<p>With the connections tool you can search for current S1 members. Connections should be made based on actual relationships between individuals and organizations in the network. To connect to a member simply send a connection request. Once the member has accepted your request, he will appear as “linked”.</p>

<p><b>Searching connections as a worker:</b>
Workers can search for Employers and Unions.</p>

<p><b>Searching connections as an employer:</b>
Employers can search for workers and Unions.</p>

