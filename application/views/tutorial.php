  <h4 id="tl-1" >My Library </h4>
 <p>Here you will find all the library pieces that are currently in your inventory. You can obtain library pieces by either purchasing them from the S1 library or by having them assigned to you by your employer. </p>
 		
 		<p>Completing Library pieces: In order to complete a library piece, open any document that is currently in your inventory. You will be required to revise all the content inside a piece and complete any related quizzes included (if present) in order to receive 100% completion mark. </p>
 		
 		<p>If you close the library piece before completion, a counter will save the percentage of the document you have completed.</p>
 		
		
 		<p id="tl-1-1"><b>Completed Documents:</b> Library pieces you have completed. </p>
 		<p id="tl-1-2"><b>Assigned Documents:</b> Library pieces that have been assigned to you by your employer.</p>
 		<p id="tl-1-3"><b>Purchased Documents:</b> Library pieces that you have purchased.</p>

 	<h4 id="tl-2">S1 Library</h4>

<p>The S1 library is where you can search the S1 database for library pieces that your may want to add to your inventory.</p>

<p id="tl-2-1"><b>Acquiring library pieces: Adding pieces to your shopping cart</b></p>

<p>Once you have found a library piece that you want to acquire, you can either click and drag the icon next to the piece, and drop it into your shopping cart or simply click on “add to cart”. </p>

<p class="well">You must complete your profile information before you can purchase any library pieces. Refer to the <a href="st-1">Getting Started with S1</a> menu for more information. </p>

<p id="tl-2-2"><b>Acquiring library pieces: Checkout Process</b></p>

 	 <div class="row-fluid">
