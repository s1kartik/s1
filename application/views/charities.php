    <h4>Threads of Life</h4>
    <img src="<?php echo $base;?>img/charities/threadsoflife.jpg" />
    <p><strong>Who they are:</strong></p>
    <p>Threads of Life is an organization supported by a network of volunteers from across the country that have been effected by workplace tragedy.  They work to heal families with the support of a community that promotes the elimination of life altering workplace incidents</p>
    
    <p>Threads of life provides assistance to families by providing a number of different services including:</p>
    <ul>
    <li>Volunteer Family Guides have been provided special training as well as have experienced tragedy themselves, to assist families to work through and survive the results of a workplace tragedy.</li>
    <li>Family Support Programs: Offer one-on-one peer support to family and friends who have been impacted by a workplace tragedy.</li>
    <li>They provide links to professional services.</li>
    <li>Advisory support for the workplace investigation and inquests.</li>
    <li>Opportunities to support injury prevention and awareness within the families community.</li>
    </ul>   
    
   <p><strong> Support/Awareness:</strong></p>
    
    <p> Threads of Life has a yearly Steps for Life Walk where families, friends, employers, co-workers, the Minister of Labour, and health and safety professionals come together on a memorial walk for awareness and fundraising.  The path of the walk is lined with workers who have been lost to workplace accidents, and allows participants to remember and learn from their accidents.  The walk takes place throughout the province on the first Sunday in May.</p>
    
 
