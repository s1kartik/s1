    <img src="<?php echo $base;?>img/hsnetwork/wsib.jpg" />

    

<p>WSIB assists workers, large companies and small business owners with many aspects of health and safety.  They provide no-fault liability insurance and access to information and training providers to assist employers to properly incorporate health and safety in their workplace.  They also aid employers with proper, safe return to work policies and procedures for successful re-entry of a worker into the workforce after an injury.</p>

<p>WSIB also assists workers by providing benefits, health care coverage and support of earnings for any lost time resulting from a workplace injury.  They also assist the workers in their transition back into the workplace after a workplace accident or injury.</p>

