    <h4>Infrastructure Health and Safety Association (IHSA)</h4>
    <img src="<?php echo $base;?>img/hsnetwork/ihsa.jpg" />

    <p>Comprises the former Construction Safety Association of Ontario (CSAO), the Electrical &amp Utilities Safety Association of Ontario (EUSA), and the Transportation Health and Safety Association of Ontario (THSAO). </p>

<p>The IHSA's core value is ensuring employee safety, development and accountability.  They are committed to continual improvement of health and safety performance and they are committed to the elimination of all occupational illness and injury in the workplace.</p>

