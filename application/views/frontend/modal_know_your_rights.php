		<h4 class="s1content_title">What are my Rights under the Act?</h4>
        <hr class="s1content_line" />
	<p class="s1content_body_section">A worker has three important rights under the Act:</p>
	
    <ul class="s1content_ul_disc">

	<li ><strong >The Right to Know:</strong> A worker has the right to know about hazards both in their workplace and in the work that they do.  The supervisor should provide every worker with instructions to ensure they are protected on the job.</li>
	<li ><strong >The Right to Participate:</strong> Each worker has the right to participate in identifying and remedying health and safety issues.  This can be done individually or with the aid of a joint health and safety committee (JHSC) or health and safety representative.</li>
	<li ><strong>The Right to Refuse:</strong> All workers have the right to refuse any work they feel is dangerous or unsafe to both themselves and other workers.</li>

	</ul>
	
		<h4  class="s1content_title">Employment Standards </h4>
		<hr class="s1content_line" />
		<p class="s1content_body_section">The Employment Standards Act, 2000 (ESA) provides the minimum standards workers and employers need to be familiar with to work in Ontario.  It identifies the responsibilities and rights of all workplace parties as well as provisions to assist employees with family responsibilities and flexibility in work arrangements. </p> 
        <p class="s1content_body_section">Some areas include: </p>
		
		<ul class="s1content_ul_disc">
		
		<li >	Hours of work</li>
	<li >	Eating and rest periods </li>
	<li >	Minimum wages and overtime </li>
	<li>	Pregnancy and Parental Leave </li>
	<li >	Public Holidays </li>
	<li>	Vacation </li>
	<li>	Termination and Severance requirements, and</li>
	<li >	Enforcement and Compliance. </li>

	</ul>
		   
