	 <p   class="s1content_subtitle">What if the Employer does not follow the ESA?</p>
	<p class="s1content_body_section">If an employee feels that the ESA is not being complied with, they can contact the Employment Standards Information Centre at 416-326-7160/1-800-531-555</p>
	<p   class="s1content_subtitle">How are Complaints Handled?</p>
	<p class="s1content_body_section">Complaints are investigated by an employment standards officer (ESO), who can provide orders against employers including orders to comply with the ESA.  ESO's can require voluntary compliance, issuing orders over pay wages, and orders to reinstate and/or compensate a notice of contravention or issue other tickets for prosecution under the Provincial Offenses Act.</p>
	<p class="s1content_subtitle">Are Sick and Bereavement Leaves covered by the ESA?</p>
	<p class="s1content_body_section">Several unpaid, job protected leaves for these types of events are available under the ESA. Some of these include emergency personal leave, Family caregiver leave, family medical leave, critically ill child care leave, crime-related child death or disappearance leave.  Some employers have paid benefit plans for sickness, bereavement and other leave of absences; these plans aren't required by the ESA.  </p>
	<p class="s1content_subtitle">If an Employee is off sick can he/she be fired?</p>
	<p class="s1content_body_section">The employee cannot be penalized for taking a day off; personal emergencies and job protection are covered under the Act.</p>
	<p class="s1content_subtitle">Do Employees need to give notice to their employers when they quit their jobs?</p>
	<p class="s1content_body_section">The ESA doesn't cover the issue of employees giving notice to the employer when they quit their jobs, except under the pregnancy and parental leave section.  Employees may be required to provide their employers with notice they are leaving.</p>
	<p class="s1content_subtitle">Can Employees choose to take their employers to court for wrongful dismissal rather then file a claim?</p>
	<p class="s1content_body_section">An employee can choose to sue an employer in a court of law for wrongful dismissal. However, an employee can't sue an employer for wrongful dismissal and have a claim for termination or severance pay investigated by the ministry for the same termination or severance. The employee must choose one procedure or the other. </p>
