		<h4 class="s1content_subtitle">Classification units and rate groups</h4>
		<p class="s1content_body_section">The WSIB groups businesses that do the same types of work into Classification Units (CUs). CUs are then placed into Rate Groups based on similar expected risks and rates of injury and illness. Each year, the WSIB sets premium rates for each Rate Group according to the group's injury and illness record and costs. Rate groups that have higher risks for injury and illness are charged higher premiums. (Your Classification Unit, Rate Group, and premium rate are printed on your Premium Remittance Form.)</p>
        <p class="s1content_body_section">See the <a href="http://www.wsib.on.ca/WSIBPortal/faces/WSIBDetailPage?cGUID=WSIB018891&rDef=WSIB_RD_ARTICLE" class="s1content_link" target="_new">Employer Classification Manual (ECM)</a></p>
        
        <h4 class="s1content_subtitle">What are premium rates made up of?</h4>
		<ul>
        	<li class="s1content_body_section">Costs of new injuries and illnesses
            </li>
            <li class="s1content_body_section">Overhead charge
            </li>
            <li class="s1content_body_section">Unfunded liability amortization charge
            </li>
            
        </ul>
		
        <h4 class="s1content_subtitle">Why do premium rates change?</h4>
		<p class="s1content_body_section">Premium rate increases and decreases are linked to injury frequencies and costs for each rate group. Overall improvements in these two factors generally translate into a decrease in the rate group premium rate.</p>	
        
        <h4 class="s1content_subtitle">Your health and safety record</h4>
		<p class="s1content_body_section">The WSIB maintains a number of <a href="http://www.wsib.on.ca/WSIBPortal/faces/WSIBDetailPage?cGUID=WSIB014490&rDef=WSIB_RD_ARTICLE" class="s1content_link" target="_new">programs and incentives</a> that reward companies with good health and safety records, and charge more to companies with poor records. So, while your basic premium rate is the same as other firms in your Rate Group, your actual premium may be higher or lower.</p>	
        
        <h4 class="s1content_subtitle">The size of your payroll</h4>
		<p class="s1content_body_section">Your premium payment depends on the <strong>annual insurable earnings</strong> in your company's payroll.</p>	
        <ul>
        	<li ><a href="http://www.wsib.on.ca/WSIBPortal/faces/WSIBDetailPage?cGUID=WSIB013921&rDef=WSIB_RD_ARTICLE" class="s1content_link" target="_new">How do I determine annual insurable earnings if I carry on a business in construction?</a>
            </li>
            <li ><a href="http://www.wsib.on.ca/WSIBPortal/faces/WSIBDetailPage?cGUID=WSIB014432&rDef=WSIB_RD_ARTICLE" class="s1content_link" target="_new">How do I determine annual insurable earnings if I do other types of work?</a>
            </li>
        </ul>
        
        <h4 class="s1content_subtitle">Calculating your premium</h4>
		<p class="s1content_body_section">To calculate your premium, multiply your firm's annual insurable earnings by your premium rate and divide by 100. Please note that if your firm is in more than one CU, you will need to calculate each CU separately.</p>	
