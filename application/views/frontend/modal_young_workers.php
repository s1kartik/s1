<!-- Begin item Staying Safe While at Work -->
	  <div class="charity-item">
		
		
		<h4 class="s1content_subtitle">Staying Safe While at Work</h4>
		<p class="s1content_body_section">When starting work there are so many things young workers need to know and often times are too inexperienced to identify what they really need to be aware of.  S1 has developed a quick check list for young workers to guide them on their journey into the workforce.
        </p>
		<ol class="s1content_ordered_list">
        	<li class="s1content_body_section"><b>Get Training! </b>The young worker must understand how to perform their jobs safely and ensure they follow all the rules and safe work practices provided by the Employer.
            </li>
            <li class="s1content_body_section"><b>Ask Questions: </b> If a young worker does not know how to do something or need to understand how to operate a vehicle, piece of machinery or other equipment, they must ask for help, ask for training or ask for clarification.  It is never wrong to ask questions and there are no “dumb” questions.</li>
            <li class="s1content_body_section"><b>Supervision: </b> Young workers need to ensure a supervisor or senior worker is available at all times to ensure the work is being performed safely and can answer any questions.</li>
            <li class="s1content_body_section"><b>Follow the rules: </b> Procedures and safe work practices, often referred to by workers as “The Rules” are there for a reason and must be followed to protect the safety of all workers.</li>
            <li class="s1content_body_section"><b>Hazard Awareness: </b> Young Workers need to be aware of all hazards around them and inform their supervisor right away when they observe unsafe work practices or hazards.</li>
           <li class="s1content_body_section"><b>Report hazards: </b> If a young worker or any worker observe contraventions, hazards or any other unsafe work practice it is very important that they notify their employer right away.  This could not only ensure the hazard is quickly controlled but could help save a life.</li> 
			<li class="s1content_body_section"><b>Wear appropriate personal protective equipment  </b>to ensure they are adequately protected from all hazards.</li> 
			<li class="s1content_body_section"><b>Only do the Assigned Job:   </b> As a young and often inexperienced worker they should only perform the job they have been trained on, they should not assist with or take initiative to perform a task they do not know how to do or have not been trained to do.</li> 
			<li class="s1content_body_section"><b>If you are injured: </b> any worker should be aware of the steps that need to be taken if they are injured on the job.  They must report any injury (serious or not) to their supervisor right away, know where to go to get first aid and make sure they get the proper medical attention.</li> 
            <li class="s1content_body_section"><b>Support System: </b> Young workers should remember that they always have a support system, if they don’t feel comfortable with brining up issues to their supervisor it is still important that they tell someone they trust so that it can be looked into and corrected. </li> 
            <li class="s1content_body_section"><b>Know Your Limits: </b> if you are feeling overwhelmed by the task provided, feel you need more training or just think that you do not have the ability to do the task ALWAYS let your supervisor know.  They will be able to assign you something you are more comfortable doing or ensure you receive more training. </li> 
            <li class="s1content_body_section"><b>Never Guess: </b> If a young worker hasn’t been trained or doesn’t know how to do something they should never assume that they will just be able to figure it out as they go along. They should never do anything without proper instruction, training or supervision. </li> 
            

            
        </ol>
		
