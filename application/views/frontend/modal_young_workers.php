	<!-- Begin item Hazard Recognition and Control: -->
	  <div class="charity-item">
		
		
		<h4 class="s1content_subtitle">Hazard Recognition and Control:</h4>
		<p class="s1content_body_section">Workers no matter how old or experienced they are will be exposed to the same hazards in their work environment.  It is important that young workers especially know about these hazards and understand how to protect themselves from them.  Employers and Supervisors have a responsibility to ensure all workers are adequately trained on hazard identification and control which includes safe handling of material and the selection, use and maintenance of protective equipment and devices.
        </p>
        <p class="s1content_body_section">To ensure young workers ask the right questions and ensure they are adequately trained and protected, here are a list of some key hazards and what they should know about them: 
        </p>
		<ol class="s1content_ordered_list">
        	<li class="s1content_body_section"><b>Chemicals, Biological or Infectious Agents: </b>Chemicals and other hazardous agents can be very dangerous if not adequately handled or stored.  All chemicals and substances must have WHMIS labels and MSDS’s that are required to be in the workplace and made available to workers on each chemical or substance being used.  Biological and infectious agents must be identified where possible as well and adequately controlled. Job specific WHMIS training must be provided by the employer, adequate PPE must be provided and procedures must be implemented and followed.
            </li>
            <li class="s1content_body_section"><b>Compressed Gas: </b> Compressed gas can be very dangerous if not adequately stored, handled or used.  It is important that Employers ensure workers are adequately trained in the safe handling, storage and use of compressed gas, that they supply an outside storage area for gas cylinders and that adequate PPE is provided and used.</li>
            <li class="s1content_body_section"><b>Temperature Extremes: </b> Regular or prolonged exposure to hot or cold temperatures can cause serious and possibly life threatening injuries. Employers are responsible for ensuring adequate hot and cold plans are in place, warm or cold rest facilities are made available and adequate clothing and PPE is used or worn.</li>
            <li class="s1content_body_section"><b>Machines: </b> Using machinery can be very dangerous, especially if they can cut, crush or strike a worker.  It is important that all machinery with access to these moving parts or any nip areas have adequate guarding on them or an emergency stop button to “lock them out” in case of an emergency. Employers must ensure these machines and their guards are adequately maintained in good working condition and that adequate training and PPE is provided to workers prior to operating the machinery.</li>
            <li class="s1content_body_section"><b>Power Tools: </b> Like machinery power tools can be very dangerous, especially when they are not adequately maintained or when workers have not been trained to use them.  Employers must ensure these power tools have adequate guards and adequate grounding to ensure workers are not electrocuted or otherwise injured from malfunctioning or unguarded equipment. They must also provide worker with adequate training and any PPE required to safely operate the equipment.</li>
           <li class="s1content_body_section"><b>Working Alone:  </b> It is very important that all workers have a “buddy system” when possible to ensure they do not work by themselves.  When this is not possible than the employer must ensure adequate measures and procedures are in place to protect workers; this includes (where possible) the use of security cameras, security systems or security guards, emergency numbers or anything else that would ensure the workers safety.  There are also some jobs that prohibit the workers from working alone, the employers should ensure they understand the jobs that MUST have more than one worker and comply with the regulations.</li> 
			
        </ol>
		
