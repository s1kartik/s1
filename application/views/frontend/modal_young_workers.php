	  <div class="charity-item">
		
		
		<h4 class="s1content_subtitle">Workers Duties Under the OHSA</h4>
		<p class="s1content_body_section">Under the OHSA workers also have duties and responsibilities.  It is important, especially for young workers that they ensure these duties and responsibilities are understood and followed for every task they perform. Some of these duties include, but are not limited to:
        </p>
		<ul>
        	<li class="s1content_body_section"><b><i>Knowing their rights</i></b> under the OHSA, these rights include:
            <ul>
            	<li class="s1content_body_section"><b>The Right to Know</b> A worker has the right to know about hazards both in their workplace and in the work that they do. The supervisor should provide every worker with instructions to ensure they are protected on the job.</li>
            	<li class="s1content_body_section"><b>The Right to Participate</b> Each worker has the right to participate in identifying and remedying health and safety issues. This can be done individually or with the aid of a joint health and safety committee (JHSC) or health and safety representative.</li>
            	<li class="s1content_body_section"><b>The Right to Refuse</b> All workers have the right to refuse any work they feel is dangerous or unsafe to both themselves and other workers.</li>

            </ul>
            </li>
            <li class="s1content_body_section">Ensuring they work safely.  This includes using the equipment required for the task being performed, ensuring it is adequately maintained and that they have been trained in how to use it.
            </li>
            <li class="s1content_body_section">Ensuring that workplace health and safety policies, programs, measures and procedures are developed, kept current, communicated to workers and always followed.
            </li>
            <li class="s1content_body_section">Ensuring they have received the training required to identify hazards, be able to control these hazards and are aware of all hazards associated with the task they are performing.</li>
            <li class="s1content_body_section">Ensuring that any hazard or contravention they observe is immediately reported to their manager and/or employer.
            </li>
            
        </ul>
		
