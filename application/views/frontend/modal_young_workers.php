<h4 class="s1content_title">What Young Workers Need to Know</h4>
    <hr class="s1content_line" />
	<p class="s1content_body_section">The Ministry of Labour has identified that on average nearly 20 young workers under the age of 25 are critically injured on the job, every day in Ontario. Young workers cannot simply depend on their Employers or Supervisors to keep them safe, they have a responsibility to themselves and fellow co-workers to protect themselves and follow all safe work practices.</p>

