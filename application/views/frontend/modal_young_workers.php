		<h4 class="s1content_subtitle">Keeping Young Workers Safe</h4>
		<p class="s1content_body_section">
Employers have the responsibility of ensure that either they or a competent worker (usually a supervisor) take the steps needed to ensure young workers are aware of all hazards, safety issues and implications to how they perform their jobs. 
        </p>
        <p class="s1content_body_section">
When starting work there are so many things young workers need to know and often times are too inexperienced to identify what they really need to be aware of.  S1 has developed a quick check list for young workers to guide them on their journey into the workforce.
        </p>
        <p class="s1content_body_title">
Training
        </p>
        <p class="s1content_body_section">
A proper orientation and training package is important especially for young workers who are new to the job and to the workforce.  An orientation of the workplace, what to do in case of an emergency, what to do when injured in the workplace and who the health and safety rep or JHSC members are is a good way to get to know the worker and help them feel more relaxed in their work environment.        </p>
        <p class="s1content_body_section">
Training for young workers is just as important, if not more to ensuring they are following safe work practices and working safely.  Young workers need to be trained thoroughly in ALL aspects of the job and the workplace as this will be their first exposure to work related hazards, chemical handling, using PPE and other aspects of the job they will be performing.  It is important that they have a complete understanding of what to do in all situations and supervisors need to ensure they are clear on the implications of not working safely.         </p>
        <p class="s1content_body_section">
Employers and supervisors should take their time to train the worker and never assume or chalk things up as common sense.        </p>
        <p class="s1content_body_title">
Supervision
        </p>
        <p class="s1content_body_section">
Ensure supervisors are introduced to the young worker prior to them starting their job.  It has been identified that lack of supervision and poor or inadequate training or the leading causes of young worker injury and death.  Supervisors should ensure they or another competent work (see the definition under the OHSA) are there to observe the young worker during their work day and provide them with direction and answer any questions the worker may have.         </p>
<p class="s1content_body_title">
Personal Protective Equipment
        </p>
        <p class="s1content_body_section">
It is important for employers and/or supervisors to identify the types of safety equipment or devices that are required in the workplace and ensure that they are made available to all workers, and that it is adequately inspected and maintained.         </p>
		
		
