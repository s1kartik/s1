		<h4 class="s1content_subtitle">Employment Standards</h4>
		<p class="s1content_body_section">
The key areas in the Employment Standards Act (ESA) that employers and supervisors must follow relate to hours of work, rest periods, overtime, minimum wage, pay day, vacation time and pay, public holidays, leave of absence, termination notice and pay and young workers.  The ESA is a law that sets minimum standards for workplaces in the province of Ontario.  If you are a unionized environment, the collective agreement(s) may have superior language that exceeds the minimum requirements set in the ESA.  Let’s look at these in more detail.        </p>
        <p class="s1content_body_section">
<span class="s1content_body_title">Hours of work:  </span>
Generally workers cannot be forced to work more than 8 hours daily or 48 hours weekly.  There may be exceptions to this, but your employer has to meet conditions outlined in the ESA (ESA section 17).       
 </p>
    <p class="s1content_body_section">
<span class="s1content_body_title">Rest periods:  </span>
Workers must have at least 11 consecutive hours off work each day and 24 consecutive hours off each week, or 48 consecutive hours every two weeks (ESA section 20). 
</p>    
    <p class="s1content_body_section">
<span class="s1content_body_title">Overtime:  </span>
Workers must be paid overtime after 44 hours worked per week at a rate of 1.5 the regular rate (ESA section22).  
</p>    
<p class="s1content_body_section">
<span class="s1content_body_title">Minimum wage: </span>
A general minimum wage applies to most workers (ESA section 23).  This minimum wage is published in this website:   <a class="s1content_link" href="http://www.labour.gov.on.ca/english/es/pubs/guide/minwage.php" target="_new">http://www.labour.gov.on.ca</a>.  The current minimum wage is indicated in the table below.  This became into effect on June 01, 2014. 
</p> 
<p class="s1content_body_section">
<span class="s1content_body_title">Pay Day:  </span>
Workers must be paid on a regularly scheduled pay day and must receive a statement that details wages earned and applicable deductions for that period (ESA section 11). 
</p> 		
<p class="s1content_body_section">
<span class="s1content_body_title">Vacation Time and Pay:  </span>
Most workers earn at least two weeks of vacation time per year.  Workers are entitled to be paid at least 4% of earned wages as vacation pay.  If you are unionized, vacation time and vacation pay may be superior to the ESA.  Refer to your collective agreement to ensure you are adhering to the language in the agreement (ESA section 33 – 35).</p> 
<p class="s1content_body_section">
<span class="s1content_body_title">Public Holidays:  </span>
Ontario has 9 public holidays.  For workers that work the public holiday they shall be paid regular pay for working the public holiday and be given another day off with regular pay, or shall be paid premium pay for working the public holiday ( ESA section 24).
</p> 
<p class="s1content_body_section">
<span class="s1content_body_title">Leave of Absence:    </span>
Generally workers have to be employed by the current employer for at least 13 weeks in order to be eligible to these unpaid, job-protected leaves (ESA section45-53):
</p> 
<ul>
        	<li class="s1content_body_section">
17 weeks of pregnancy leave.            
 </li>
            <li class="s1content_body_section">
35 or 37 weeks of parental leave. 
            </li>
            <li class="s1content_body_section">
10 days each calendar year of personal emergency leave for personal illness, injury or medical emergency, or for the death, illness, injury, medical emergency of or urgent matter concerning certain family members.             </li>
            <li class="s1content_body_section">
8 weeks in a 26-week period of family medical leave to care for or support certain family members such as the worker’s spouse, parent, step-parent, or foster parent, a child, step-child or foster child of the worker or spouse and any other signification person you consider as family.
</li>
            <li class="s1content_body_section">
Up to 37 weeks within a 52 week period leave to care for a critically ill child.   A “child” means a child, step-child, foster child or child who is under legal guardianship, and who is under 18 years of age.  This does not include children with chronic illness.
            </li>
            <li class="s1content_body_section">
Child crime related death or disappearance leave.  If a worker has been employed by the employer for 6 month, the worker is entitled up to 104 weeks of leave for a child crime related death, and up to 57 weeks for a crime-related disappearance of a child. 
			</li>
            <li class="s1content_body_section">
Risk of dying within a period of 26 weeks. 
            </li>
            <li class="s1content_body_section">
Declared emergency leave.
            </li>
        </ul>
        <p class="s1content_body_section">
<span class="s1content_body_title">Termination of Notice:  </span>
Generally if the worker has been employed for 3 months or more and his job is terminated, the employer must give the worker advance written notice, or termination pay instead of notice, or a combination of both (ESA section 54-67).
</p>	
<p class="s1content_body_section">

If there is a collective agreement in place review the provisions that apply to the areas covered above.  The ESA provides minimum requirements.  The collective agreement may have superior language.
