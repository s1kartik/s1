    <p><strong>Who they are:</strong></p>
    <p>This not-for-profit cross-Canada program works to help eliminate needless injuries and assist in the prevention of workplace fatalities.  This program is for young Canadians who have the power to influence and change the workplace culture if they are trained and educated to do so.</p>
    
